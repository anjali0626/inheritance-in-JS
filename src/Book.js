class Book {

  constructor() {
    this.isbn = null;
    this.title = null;
    this.author = null;
    this.price = null;
  }

  getIsbn() {
    return this.isbn;
  }

  getTitle() {
    return this.title;
  }

  getAuthor() {
    return this.author;
  }

  getPrice() {
    return this.price;
  }

};